import numpy as np

#Basic operations
a = np.array([1, 2, 3])
b = np.array([4, 5, 6])

addition = a + b      # Element-wise addition
print(addition)
subtraction = a - b   # Element-wise subtraction
print(subtraction)
multiplication = a * b  # Element-wise multiplication
print(multiplication)
division = a / b      # Element-wise division
print(division)
